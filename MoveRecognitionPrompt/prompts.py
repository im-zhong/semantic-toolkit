"""
语步识别提示词模板

语步（Move）是学术写作分析中的重要概念，指文本中实现特定交际功能的修辞单元。
本模板用于识别科技文献中的五种语步：研究背景、研究目的、研究方法、研究结果、研究结论。
"""

# 系统提示词
SYSTEM_PROMPT = """你是一位专业的科技文献分析专家，精通学术写作修辞分析。
你的任务是分析科技文献摘要，识别并标注其中的语步结构。

语步（Move）是指文本中实现特定交际功能的修辞单元。在科技文献中，常见的语步包括：

1. **研究背景（Background）**：介绍研究领域现状、存在的问题、研究的重要性等
   - 关键词：目前、现有、然而、但是、存在问题、已有研究、众所周知等

2. **研究目的（Purpose）**：说明本研究要解决的问题、目标、创新点等
   - 关键词：本文、本研究、旨在、目的、提出、设计、开发等

3. **研究方法（Method）**：描述采用的方法、技术、实验设计等
   - 关键词：采用、使用、提出、方法、模型、算法、实验、数据集等

4. **研究结果（Result）**：展示研究发现、实验结果、性能指标等
   - 关键词：结果表明、实验显示、发现、达到、提升、准确率、效率等

5. **研究结论（Conclusion）**：总结研究贡献、意义、局限性、未来展望等
   - 关键词：结论、表明、验证、证明、意义、贡献、未来、展望等

请严格按照上述定义进行语步识别，确保分类准确。"""

# 用户提示词模板
USER_PROMPT_TEMPLATE = """请分析以下科技文献摘要，识别并标注其中的语步结构。

【待分析摘要】
{abstract}

【输出要求】
请按照以下格式输出，每个语步下列出对应的原文句子：

## 研究背景
- [原文句子]

## 研究目的
- [原文句子]

## 研究方法
- [原文句子]

## 研究结果
- [原文句子]

## 研究结论
- [原文句子]

注意：
1. 如果某个语步在摘要中没有对应内容，标注为"无"
2. 一个句子可能同时属于多个语步，请根据其主要功能判断
3. 保持原文句子的完整性，不要修改或省略
4. 每个句子只需标注一次，归类到其主要语步中"""


# 结构化输出提示词（用于JSON格式输出）
STRUCTURED_PROMPT_TEMPLATE = """请分析以下科技文献摘要，识别并标注其中的语步结构。

【待分析摘要】
{abstract}

【输出格式】
请严格按照以下JSON格式输出，不要输出其他内容：

```json
{{
    "background": ["句子1", "句子2"],
    "purpose": ["句子1"],
    "method": ["句子1", "句子2"],
    "result": ["句子1"],
    "conclusion": ["句子1"]
}}
```

注意：
1. 如果某个语步没有对应句子，使用空数组 []
2. 一个句子只归类到其主要语步中
3. 保持原文句子完整"""


def get_prompt(abstract: str, structured: bool = False) -> tuple[str, str]:
    """
    获取完整的提示词

    Args:
        abstract: 待分析的摘要文本
        structured: 是否使用结构化输出格式

    Returns:
        (system_prompt, user_prompt)
    """
    if structured:
        user_prompt = STRUCTURED_PROMPT_TEMPLATE.format(abstract=abstract)
    else:
        user_prompt = USER_PROMPT_TEMPLATE.format(abstract=abstract)

    return SYSTEM_PROMPT, user_prompt


